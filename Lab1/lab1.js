let fahrenheit = 55; //this a average temperature in Kathmandu(capital city of nepal) in the month of september//
console.log("Temperature (Fahrenheit): " + fahrenheit);//printing the temperature in fahrenheit//
let celsius = (fahrenheit - 32) * 5 / 9; //printing the temperature in celsius//
console.log("Temperature (Celsius): " + celsius);//formula to convert fahrenheit to celsius//
let kelvin = (fahrenheit + 459.67) * 5 / 9; //formula to convert fahrenheit to kelvin//
console.log("Temperature (Kelvin): " + kelvin);//printing the temperature in kelvin//